package com.dicfree.pda.base;


import com.dicfree.pda.base.bean.BaseResponseBean;

import autodispose2.AutoDispose;
import autodispose2.ObservableSubscribeProxy;
import autodispose2.ScopeProvider;
import io.reactivex.rxjava3.android.schedulers.AndroidSchedulers;
import io.reactivex.rxjava3.core.Observable;
import io.reactivex.rxjava3.core.ObservableSource;
import io.reactivex.rxjava3.core.ObservableTransformer;
import io.reactivex.rxjava3.schedulers.Schedulers;

public class RxScheduler {

    public static <T> ObservableTransformer<T, T> io_mains() {
        return new ObservableTransformer<T, T>() {
            @Override
            public ObservableSource<T> apply(Observable<T> upstream) {
                return upstream.subscribeOn(Schedulers.io())
                        .observeOn(AndroidSchedulers.mainThread());
            }
        };
    }

    public static <T> ObservableSubscribeProxy<BaseResponseBean<T>> rxRequest(Observable<BaseResponseBean<T>> observable, final ScopeProvider provider) {
        return observable
                .compose(RxScheduler.io_mains())
                .retryWhen(new RxRetryHandler())
                .to(AutoDispose.autoDisposable(provider));
    }


}
